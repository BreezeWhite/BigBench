def calc_pi_bbp(start_idx: int, end_idx: int) -> str:
    pi = Decimal(0)
    for i in range(start_idx, end_idx):
        common = Decimal(i) * 8
        a = 4 / (common + 1)
        b = 2 / (common + 4)
        c = 1 / (common + 5)
        d = 1 / (common + 6)
        
        s = a - b - c - d
        for _ in range(i):
            s /= 16
        pi += s
    return str(pi)

